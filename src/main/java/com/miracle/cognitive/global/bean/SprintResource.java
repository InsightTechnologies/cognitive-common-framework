package com.miracle.cognitive.global.bean;

public class SprintResource {
	private String sprintName;
	private Integer resourceHours;

	/**
	 * @return the sprintName
	 */
	public String getSprintName() {
		return sprintName;
	}

	/**
	 * @param sprintName the sprintName to set
	 */
	public void setSprintName(String sprintName) {
		this.sprintName = sprintName;
	}

	/**
	 * @return the resourceHours
	 */
	public Integer getResourceHours() {
		return resourceHours;
	}

	/**
	 * @param resourceHours the resourceHours to set
	 */
	public void setResourceHours(Integer resourceHours) {
		this.resourceHours = resourceHours;
	}
}
